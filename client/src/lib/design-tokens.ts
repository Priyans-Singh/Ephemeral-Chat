// Utility function to get CSS custom property
export function getCSSVar(property: string): string {
  return `var(--${property})`;
}

// Utility function to create theme-aware styles
export function createThemeAwareStyles(lightValue: string, darkValue: string): string {
  return `light-dark(${lightValue}, ${darkValue})`;
}

